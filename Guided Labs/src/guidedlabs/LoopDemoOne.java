//Example 1: Display a Text Five Times

package guidedlabs;

public class LoopDemoOne {

	public static void main(String[] args) {
		// for loop syntax:
		// for (initialization; termination;increment) {
		// statement(s)
		// }

		// Program to print a text 5 times
		int n = 5;
		// for loop
		for (int i = 1; i <= n; ++i) {
			System.out.println("Java is fun");
		}
	}

}

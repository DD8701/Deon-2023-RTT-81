package variables;

public class Seven {

	public static void main(String[] args) {

		// declares a named constant, final or static. final cannot change, static can be changed.
		final double PI = 3.14;
		
		// uses named constant in a calculation
		double a = 9;
		double b = 5;
		double x = a * b * PI;
		
		// prints the result
		System.out.println(x);
		
		
		

	}

}
